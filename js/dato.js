class Dato{
    constructor(descripcion, valor){
        this.descripcion = descripcion;
        this.valor = valor;
    }

    get Descripcion(){
        return this.descripcion;
    }

    set Descripcion(descripcion){
        this.descripcion = descripcion;
    }

    get Valor(){
        return this.descripcion;
    }

    set Valor(valor){
        this.valor = valor;
    }
}